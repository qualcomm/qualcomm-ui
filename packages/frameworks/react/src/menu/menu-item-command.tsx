import type {ReactElement, ReactNode} from "react"

import {
  type ElementRenderProp,
  PolymorphicElement,
} from "@qualcomm-ui/react-core/system"
import {mergeProps} from "@qualcomm-ui/utils/merge-props"

import {useQdsMenuContext} from "./qds-menu-context"

export interface MenuItemCommandProps extends ElementRenderProp<"kbd"> {
  /**
   * React {@link https://react.dev/learn/passing-props-to-a-component#passing-jsx-as-children children} prop.
   */
  children?: ReactNode
}

export function MenuItemCommand(props: MenuItemCommandProps): ReactElement {
  const context = useQdsMenuContext()
  const mergedProps = mergeProps(context.getItemCommandBindings(), props)

  return <PolymorphicElement as="kbd" {...mergedProps} />
}
